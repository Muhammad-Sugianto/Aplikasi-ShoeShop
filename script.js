// Home
// Ambil referensi ke elemen berdasarkan ID
const homeSection = document.getElementById("home");

// Fungsi untuk mengganti warna latar belakang secara acak
function changeBackgroundColor() {
    const colors = ["#f2f2f2", "#e6e6e6", "#d9d9d9", "#cccccc"];
    const randomIndex = Math.floor(Math.random() * colors.length);
    homeSection.style.backgroundColor = colors[randomIndex];
}

// Panggil fungsi changeBackgroundColor setiap 3 detik
setInterval(changeBackgroundColor, 3000);







